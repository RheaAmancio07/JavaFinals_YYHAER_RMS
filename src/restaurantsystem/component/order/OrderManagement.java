    private void viewCartTable() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/amancio_rms", "root", "");
            PreparedStatement pstm = con.prepareStatement("SELECT * FROM cart");
            ResultSet Rs = pstm.executeQuery();

            DefaultTableModel model = (DefaultTableModel) reciptArea.getModel();

            while (Rs.next()) {
                model.addRow(new Object[]{Rs.getString(2), Rs.getInt(3), Rs.getInt(4)});
            }

            Rs = pstm.executeQuery("SELECT SUM(cost) from cart");
            Rs.next();
            int total = Rs.getInt(1);
            totalPriceField.setText(String.valueOf(total));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }

