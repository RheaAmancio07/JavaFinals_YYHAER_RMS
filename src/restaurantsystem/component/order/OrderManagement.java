    private void orderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButtonActionPerformed

        String newItemId = itemIDToOrderField.getText();
        String newItemQuantityAsString = itemOrderQuantityField.getText();

        if (newItemId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter item id to add to cart");
        } else if (newItemQuantityAsString.isEmpty()
                || !newItemQuantityAsString.chars().allMatch(Character::isDigit)) {
            JOptionPane.showMessageDialog(this, "Please enter valid quantity to add to cart");

        } else {
            int newItemQuantity = Integer.parseInt(newItemQuantityAsString);
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/amancio_rms", "root", "");
                PreparedStatement pstm = con.prepareStatement("SELECT * FROM item WHERE item_id = '" + newItemId + "'");
                ResultSet Rs = pstm.executeQuery();
                if (Rs.next()) {
                    int id = Rs.getInt(1);
                    String name = Rs.getString(2);
                    int price = Rs.getInt(3);
                    int quantity = Rs.getInt(4);
                    if (newItemQuantity > quantity) {
                        JOptionPane.showMessageDialog(this, "Reach the maximum quantity!");
                    } else {
                        int newQuantity = quantity - newItemQuantity;
                        int total = newItemQuantity * price;
                        try {
                            Class.forName("com.mysql.jdbc.Driver"); //load the driver
                            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/amancio_rms", "root", ""); //establishes the connection

                            // the mysql insert statement
                            String query = " insert into cart (name, quantity,cost)" + " values (?, ?, ?)";
                            String queryUpdate = "UPDATE item SET quantity ='" + newQuantity + "' WHERE item_id ='" + id + "'";
                            // create the mysql insert preparedstatement

                            PreparedStatement preparedStmt = conn.prepareStatement(query);
                            PreparedStatement preparedStmtUpdate = conn.prepareStatement(queryUpdate);

                            preparedStmt.setString(1, name);

                            preparedStmt.setInt(2, newItemQuantity);

                            preparedStmt.setInt(3, total);

                            // execute the preparedstatement
                            preparedStmtUpdate.executeUpdate();
                            preparedStmt.execute();

                            conn.close();
                            JOptionPane.showMessageDialog(null, "Order successfully ..");

                            itemIDToOrderField.setText("");
                            itemOrderQuantityField.setText("");

                            DefaultTableModel tableModel = (DefaultTableModel) reciptArea.getModel();
                            tableModel.setRowCount(0);
                            DefaultTableModel dm = (DefaultTableModel) viewItems3.getModel();
                            dm.setRowCount(0);
                            viewCartTable();
                            viewTable();

                        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                            JOptionPane.showMessageDialog(null, e);
                            System.out.println(e);
                        }
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Item does not exist!");
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }


    }//GEN-LAST:event_orderButtonActionPerformed

